using System;

namespace Calculator_Logic
{
    public class Calculator

    {
        public double Add(double left, double right)

        {
            return left + right;
        }

        public double Subtract(double left, double right)

        {
            return left - right;
        }

        public double Multiply(double left, double right)

        {
            return left * right;
        }

    }
}